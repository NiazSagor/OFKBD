package com.ofk.bd.HelperClass;

public class Activity {

    String title;
    String url;


    public Activity() {
    }

    public Activity(String title, String url) {
        this.title = title;
        this.url = url;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
